    private int getDays(){
        int day = 0;
        String dd = JOptionPane.showInputDialog("Days");
        try{
            int d = Integer.parseInt(dd);
            day = d;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Incorrect days");
        }
        
        return day;
    }

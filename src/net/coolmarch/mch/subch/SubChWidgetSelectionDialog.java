    private void doIntradayPriceVolume(){
        JPanel p = new JPanel();
        p.setLayout(new BorderLayout());
        int days = Integer.parseInt(this.tfIntradayPriceVolume.getText());
        CUPriceVolumeChart ch = new CUPriceVolumeChart(symbol, days);
        ChartPanel cp = ch.getChartPanel2();
        Color c = p.getBackground();
        cp.setBackground(c);
        cp.getChart().getXYPlot().setBackgroundPaint(c);
        cp.getChart().setBackgroundPaint(c);
        CMResource cmr = new CMResource();
        ImageIcon icon = cmr.getIcon("chartbgimg.png");
        Image image = icon.getImage();
        cp.getChart().setBackgroundImage(image);
        cp.getChart().setBackgroundImageAlignment(Align.CENTER);
        cp.getChart().getXYPlot().setBackgroundAlpha(0.3f);
        p.add(cp);
        p.setBorder(null);
        JFrame frame = new JFrame("Intraday:Price/Volume:"+symbol);
        frame.getContentPane().add(p);
        frame.setSize(w_width, w_height);
        frame.setLocationRelativeTo(null);
        frame.setVisible(true);
        frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
    }
